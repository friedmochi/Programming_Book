// Program to read an integer
#include "../../std_lib_facilities.h"

int main()
{
    cout << "Please enter a floating-point value: \n";
    int n;
    cin >> n;

    cout << "n== " << n;
    cout << "\n n+1 == " << n+1;
    cout << "\n three times n == " << 3*n;
    cout << "\n twice n == " << n + n;
    cout << "\n n squared == " << n * n;
    cout << "\n n half of n == " << n / 2;
    cout << "\n n square root of n == " << sqrt(n);
    cout << '\n';


    return 0;
   
}